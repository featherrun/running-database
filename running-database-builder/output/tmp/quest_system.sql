CREATE TABLE `quest_system` (
  `quest_id` int(10) NOT NULL COMMENT '任务ID',
  `quest_name` varchar(255) NOT NULL COMMENT '任务名',
  `quest_type` tinyint(3) NOT NULL COMMENT '任务类型',
  `quest_color` tinyint(3) NOT NULL COMMENT '任务品质',
  `next_quest` int(10) NOT NULL COMMENT '后续任务ID',
  `quest_group` tinyint(3) NOT NULL COMMENT '任务组',
  `quest_group_order` tinyint(3) NOT NULL COMMENT '任务组顺序',
  `quest_accept_prequest` int(10) NOT NULL COMMENT '任务接受要求完成任务',
  `quest_accept_prefunc` int(10) NOT NULL COMMENT '任务接受要求开放功能',
  `quest_accept_minlevel` tinyint(3) NOT NULL COMMENT '任务接受等级下限',
  `quest_accept_maxlevel` tinyint(3) NOT NULL COMMENT '任务接受等级上限',
  `quest_about` varchar(255) NOT NULL COMMENT '任务简介',
  `quest_checkpoint_type` tinyint(3) NOT NULL COMMENT '任务检查点类型',
  `quest_checkpoint_data` varchar(255) NOT NULL COMMENT '任务检查点数据',
  `quest_checkpoint_value` int(10) NOT NULL COMMENT '任务检查点值',
  `quest_checkpoint_path` varchar(255) NOT NULL COMMENT '任务检查点寻径',
  `quest_reward` int(10) NOT NULL COMMENT '任务奖励',
  `quest_active_point` tinyint(3) NOT NULL COMMENT '增加活跃点',
  PRIMARY KEY (`quest_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='任务模板表';